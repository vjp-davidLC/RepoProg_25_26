package operaciones;

/**
 *
 * @author alumno
 */
public class Operaciones {
    
    public static void raizCuadrada() {
    
        System.out.println("La raiz cuadrada de 64 es = "+Math.sqrt(64));
    
    }
    
    public static void ochoElevadoAlCubo() {
    
        System.out.println("8 elevado al cubo es = "+Math.pow(8, 3));
    
    }
    
    public static void exponencialDos () {
    
        System.out.println("Exponencial de 2 es = "+Math.exp(2));
    
    }
    
    public static void logaritmo () {
    
        System.out.println("Logaritmo de 2,71828 es = "+Math.log(2.71828));
    
    }
    
    public static void menor () {
    
        System.out.println("El menor de 2 y de 3 es = "+Math.min(2, 3));
        
    }
    
    public static void absoluto () {
    
        System.out.println("Valor absoluto de -4,5 es = "+Math.abs(-4.5));
        
    }
    
    public static void redondeo () {
    
        System.out.println("El redondeo de -4,5 con ROUND es = "+Math.round(-4.5));
    
    }
    
    public static void seno () {
    
        System.out.println("El seno de 45 grados es = "+Math.sin(45));
        
    }
    
}
